# Scripts de Respaldo para Directorios y Base de Datos  
## Una solucion compuestas por varios scripts que configurada correctamente permite gestionar el respaldo de los directorios y bases de datos (PostgreSQL y MySQL), contando ademas con la bondad de generar por cada ejecucion un registro de bitacora, el cual posteriormente podra ser remitido via correo electronico.

